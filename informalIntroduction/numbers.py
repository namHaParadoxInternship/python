# Division
# classic division
print(17 / 3)

# floor division
baseNum = 17
devBy = 3
divResult = baseNum // devBy
print(divResult)

# calculate remainder of division
remainder = baseNum % devBy
print(remainder)

# re-calculate base number
print(divResult * devBy + remainder)


